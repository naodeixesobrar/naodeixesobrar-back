package br.com.naodeixesobrar.entity;
 
import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;
 
@Embeddable
public class CompanyUserPKEntity implements Serializable{
 
	private static final long serialVersionUID = -3022960835531107268L;

	@Column(name="companyid")
	private Integer companyId;
 
	@Column(name="userid")	
	private Integer userId;

	public Integer getCompanyId() {
		return companyId;
	}

	public void setCompanyId(Integer companyId) {
		this.companyId = companyId;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((companyId == null) ? 0 : companyId.hashCode());
		result = prime * result + ((userId == null) ? 0 : userId.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		CompanyUserPKEntity other = (CompanyUserPKEntity) obj;
		if (companyId == null) {
			if (other.companyId != null)
				return false;
		} else if (!companyId.equals(other.companyId))
			return false;
		if (userId == null) {
			if (other.userId != null)
				return false;
		} else if (!userId.equals(other.userId))
			return false;
		return true;
	}
 

}